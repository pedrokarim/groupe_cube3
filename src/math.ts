/**
 * Additionne deux nombres
 */
export function add(a: number, b: number): number {
  return a + b;
}

/**
 * Multiplie deux nombres
 */
export function multiply(a: number, b: number): number {
  return a * b;
}

// Test rapide
console.log("2 + 3 =", add(2, 3));
console.log("4 x 5 =", multiply(4, 5));


